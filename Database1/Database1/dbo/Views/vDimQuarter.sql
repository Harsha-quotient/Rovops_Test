






CREATE VIEW [dbo].[vDimQuarter]
AS

SELECT DISTINCT QUARTER_ID, 
	QUARTER_START,
	QUARTER_END,
	QUARTER_CAPTION,
	QUARTER_OF_YEAR_NUMBER,
	LAST_QUARTER_ID,
	LAST_YEAR_SAME_QUARTER_ID,
	YEAR_ID 
FROM MSTRReportingDB.DBO.MDM_DAY_DIM WITH (NOLOCK)
WHERE DATE_ID >= 20160101 AND DATE_ID < 20230101






