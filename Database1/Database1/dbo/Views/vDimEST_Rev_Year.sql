







CREATE VIEW [dbo].[vDimEST_Rev_Year]
AS

SELECT DISTINCT 
	YEAR_ID AS EST_REV_YEAR_ID
	,YEAR_START AS EST_REV_YEAR_START
	,YEAR_END AS EST_REV_YEAR_END
	,LAST_YEAR_ID AS EST_REV_LAST_YEAR_ID
FROM MSTRReportingDB.DBO.MDM_DAY_DIM WITH (NOLOCK)
WHERE DATE_ID >= 20160101 AND DATE_ID < 20230101







